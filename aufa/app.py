def resep_cheesecake_no_bake():
    print("Resep Cheesecake No Bake Simple\n")
    print("Bahan:")
    print("- 150 gram biskuit (marie/digestive), haluskan")
    print("- 75 gram mentega, lelehkan")
    print("- 250 gram cream cheese (suhu ruang)")
    print("- 100 gram gula pasir")
    print("- 200 ml whipped cream")
    print("- 1 sdt vanila ekstrak")
    print("- 1 sdm air jeruk lemon (opsional)\n")

    print("Cara Membuat:")
    print("1. Campur biskuit halus dan mentega leleh, aduk rata.")
    print("2. Tekan campuran biskuit ke dasar loyang, ratakan dan padatkan. Simpan di kulkas sementara.")
    print("3. Kocok cream cheese dan gula sampai lembut dan creamy.")
    print("4. Tambahkan vanila ekstrak dan air jeruk lemon, aduk rata.")
    print("5. Kocok whipped cream sampai soft peak, lalu campurkan perlahan ke adonan cream cheese.")
    print("6. Tuang adonan ke atas dasar biskuit, ratakan permukaannya.")
    print("7. Simpan di kulkas minimal 4 jam sampai cheesecake set.")
    print("8. Setelah set, cheesecake siap disajikan. Bisa beri topping buah sesuai selera.")

if __name__ == "__main__":
    resep_cheesecake_no_bake()
